<template>
  <v-card elevation="2" border class="ma-5">
    <v-card-title>
      Подобрать рейсы для акта выполненных работ №
      {{ paymentInvoice.number || 'б/н' }}
    </v-card-title>
    <v-card-subtitle>
      <b>{{ clientName }} </b>
    </v-card-subtitle>
    <v-card-actions>
      <v-btn class="ma-2" @click="cancelHandler">Закрыть</v-btn>
      <v-btn
        class="ma-2"
        color="primary"
        :disabled="!selectedOrders.length"
        @click="addToInvoiceHandler"
      >
        Добавить в акт
      </v-btn>
    </v-card-actions>
    <v-card-text>
      <pick-orders-settings
        v-model="settings"
        :allHeaders="allHeaders"
        @update-headers="updateActiveHeaders"
        @refresh="refreshHandler"
      />

      <orders-table
        v-model="selectedOrders"
        v-model:listOptions="settings.listOptions"
        show-select
        itemIdField="orderId"
        :items="items"
        :headers="headers"
        :loading="loading"
        @add-item="addOrderToInvoice"
        @open-docs-dialog="openDocsDialog"
      />
      <v-dialog v-model="docDialog" max-width="1300" persistent>
        <order-docs-list
          :orderId="editableOrderId"
          :docs="editableDocs"
          @save="saveDocDialog"
          @cancel="cancelDocDialog"
        />
      </v-dialog>
    </v-card-text>
  </v-card>
</template>
<script lang="ts">
//@ts-nocheck
import { defineComponent } from 'vue'
import { ref, computed } from 'vue'

import { OrdersTable, useOrderDocs, OrderDocsList } from '@/entities/order'
import { PickOrdersSettings } from '@/entities/paymentInvoice'
import { PickOrdersForPaymentInvoiceHeaders } from '@/shared/constants'
import { PaymentInvoiceService } from '@/shared/services'
import store from '@/store'

import { useListData } from './model'

export default defineComponent({
  name: 'PickOrdersForPaymentInvoiceFeature',
  components: { PickOrdersSettings, OrdersTable, OrderDocsList },
  props: {
    paymentInvoice: {
      type: Object,
      required: true,
    },
  },
  setup({ paymentInvoice }, ctx) {
    const headers = ref([])
    const selectedOrders = ref([])
    const { loading, settings, items, refresh } = useListData(paymentInvoice)

    const {
      editableOrderId,
      openDocsDialog,
      docDialog,
      editableDocs,
      saveDocDialog,
      cancelDocDialog,
    } = useOrderDocs()

    const client = computed(() =>
      store.getters.partnersMap.get(paymentInvoice.clientId)
    )

    const clientName = computed(() => {
      return client.value.name || '-'
    })

    const selectedOrdersIds = computed(() =>
      selectedOrders.value.map((i) => i._id)
    )

    function updateActiveHeaders(val) {
      headers.value = val
    }

    function cancelHandler() {
      selectedOrders.value = []
      ctx.emit('cancel')
    }

    function refreshHandler() {
      refresh()
      selectedOrders.value = []
    }

    async function addOrderToInvoice(orderId) {
      await PaymentInvoiceService.addOrdersToPaymentInvoice({
        orders: [orderId],
        paymentInvoiceId: paymentInvoice._id,
      })

      selectedOrders.value = selectedOrders.value.filter(
        (i) => i._id !== orderId
      )
      refresh()
    }

    async function addToInvoiceHandler() {
      await PaymentInvoiceService.addOrdersToPaymentInvoice({
        orders: selectedOrdersIds.value,
        paymentInvoiceId: paymentInvoice._id,
      })

      selectedOrders.value = []
      refresh()
    }
    return {
      cancelHandler,
      addToInvoiceHandler,
      loading,
      updateActiveHeaders,
      allHeaders: PickOrdersForPaymentInvoiceHeaders(),
      settings,
      items,
      headers,
      refreshHandler,
      selectedOrders,
      selectedOrdersIds,
      clientName,
      client,
      addOrderToInvoice,

      editableOrderId,
      openDocsDialog,
      docDialog,
      editableDocs,
      saveDocDialog,
      cancelDocDialog,
    }
  },
})
</script>
