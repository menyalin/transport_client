<template>
  <div>
    <v-text-field
      v-model="emailStr"
      dense
      label="Поиск пользователя по EMAIL"
      outlined
      clearable
      :disabled="loading"
      :loading="loading"
      :errorMessages="errorMessages"
      @change="changeHandler"
    />
    <div v-if="candidate.name" class="text-h5 mb-5">
      <small>Имя:</small> {{ candidate.name }}
    </div>
  </div>
</template>
<script lang="ts">
//@ts-nocheck
import { defineComponent } from 'vue'
import { ref } from 'vue'

import { WorkerService } from '@/shared/services'

export default defineComponent({
  name: 'PickUserFeature',
  model: {
    prop: 'value',
    event: 'change',
  },
  setup(_props, ctx) {
    const emailStr = ref('')
    const loading = ref(false)
    const candidate = ref({})
    const errorMessages = ref([])

    function setCandidate(val) {
      candidate.value = val
      ctx.emit('change', { ...val })
    }

    async function changeHandler(value) {
      errorMessages.value = []
      if (value) await searchUser(value)
    }

    async function searchUser(value) {
      try {
        loading.value = true
        const candidate = await WorkerService.getUserByEmail(value)
        loading.value = false
        if (!candidate) {
          errorMessages.value.push('Пользователь не найден!')
          setCandidate({})
          return
        } else {
          setCandidate(candidate)
        }
      } catch (e) {
        this.$store.commit('setError', e.message)
        loading.value = false
      }
    }

    return {
      loading,
      emailStr,
      changeHandler,
      errorMessages,
      candidate,
    }
  },
})
</script>
