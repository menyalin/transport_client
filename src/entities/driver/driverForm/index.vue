<script lang="ts">
//@ts-nocheck
import { required } from '@vuelidate/validators'
import { defineComponent } from 'vue'
import { mapGetters } from 'vuex'

import {
  ButtonsPanel,
  DateTimeInput,
  AdditionalNotifications,
} from '@/shared/ui'

import AppMedBook from './medBook.vue'

export default defineComponent({
  name: 'DriverForm',
  components: {
    ButtonsPanel,
    DateTimeInput,
    AppMedBook,
    AdditionalNotifications,
  },
  props: {
    driver: {
      type: Object,
    },
    displayDeleteBtn: {
      type: Boolean,
      default: false,
    },
  },
  data() {
    return {
      loading: false,
      medBook: {},
      additionalNotifications: [],
      initialFormState: null,
      form: {
        tkName: null,
        surname: null,
        name: null,
        patronymic: null,
        passportId: null,
        passportIssued: null,
        passportDate: null,
        licenseId: null,
        licenseDate: null,
        licenseCategory: null,
        driverCardId: null,
        driverCardPeriod: null,
        hasScans: null,
        phone: null,
        phone2: null,
        employmentDate: null,
        dismissalDate: null,
        recommender: null,
        birthday: null,
        isBrigadier: false,
        isMechanic: false,
        hideInFines: false,
        isCalcSalary: true,
        inn: null,
      },
    }
  },
  computed: {
    ...mapGetters(['tkNames']),
    disabledSubmitForm() {
      return (
        !this.$store.getters.hasPermission('driver:write') ||
        this.$v.$invalid ||
        !this.formChanged ||
        this.loading
      )
    },

    nameErrors() {
      const errors = []
      if (this.$v.form.name.$dirty && this.$v.form.name.$invalid)
        errors.push('Имя не может быть пустым')
      return errors
    },
    surnameErrors() {
      const errors = []
      if (this.$v.form.surname.$dirty && this.$v.form.surname.$invalid)
        errors.push('Имя не может быть пустым')
      return errors
    },
    formChanged() {
      return this.initialFormState !== JSON.stringify(this.formState)
    },
    formState() {
      return {
        ...this.form,
        medBook: this.medBook,
        company: this.$store.getters.directoriesProfile,
        additionalNotifications: this.additionalNotifications,
      }
    },
  },
  watch: {
    driver: {
      immediate: true,
      handler: function (val) {
        if (val) this.setFormFields(val)
      },
    },
  },
  validations: {
    form: {
      tkName: { required },
      surname: { required },
      name: { required },
      patronymic: {},
      passportId: {},
      passportIssued: {},
      passportDate: {},
      licenseId: {},
      licenseDate: {},
      licenseCategory: {},
      driverCardId: {},
      driverCardPeriod: {},
      hasScans: {},
      phone: {},
      phone2: {},
      employmentDate: {},
      dismissalDate: {},
      recommender: {},
      birthday: {},
      inn: {},
    },
  },
  mounted() {
    this.initialFormState = JSON.stringify(this.formState)
  },
  methods: {
    submit() {
      this.$emit('submit', this.formState)
      // this.resetForm()
    },
    cancel() {
      this.resetForm()
      this.$emit('cancel')
    },
    setFormFields(val) {
      const keys = Object.keys(this.form)
      this.medBook = val.medBook
      if (val.additionalNotifications)
        this.additionalNotifications = val.additionalNotifications
      keys.forEach((key) => {
        this.form[key] = val[key]
        if (val.tkName?._id) this.form.tkName = val.tkName._id
      })
    },
    resetForm() {
      const keys = Object.keys(this.form)
      this.medBook = { ...{} }
      this.additionalNotifications = []
      keys.forEach((key) => {
        this.form[key] = null
      })
    },
  },
})
</script>
