<template>
  <div>
    <buttons-panel
      panel-type="form"
      :disabled-submit="disabledSubmitForm"
      @cancel="cancel"
      @submit="submit"
    />

    <div>
      <div class="row-wrapper tk-name">
        <v-select
          v-model="$v.form.tkName.$model"
          :items="tkNames"
          item-title="name"
          item-value="_id"
          label="ТК"
          density="compact"
          variant="outlined"
        />
      </div>

      <div class="row-wrapper driver-name">
        <v-text-field
          v-model.trim="$v.form.surname.$model"
          variant="outlined"
          label="Фамилия"
          density="compact"
          :error-messages="surnameErrors"
        />
        <v-text-field
          v-model.trim="$v.form.name.$model"
          variant="outlined"
          label="Имя"
          density="compact"
          :error-messages="nameErrors"
        />
        <v-text-field
          v-model.trim="$v.form.patronymic.$model"
          variant="outlined"
          label="Отчество"
          density="compact"
          :error-messages="nameErrors"
        />
        <DateTimeInput
          v-model="$v.form.birthday.$model"
          label="Дата рождения"
          hide-time-input
          hide-prepend-icon
        />
      </div>

      <div class="row-wrapper">
        <div class="passport">
          <div>
            <v-text-field
              v-model.trim="$v.form.passportId.$model"
              variant="outlined"
              hide-details
              label="Номер паспорта"
              density="compact"
              class="pb-3"
            />
            <DateTimeInput
              v-model="$v.form.passportDate.$model"
              label="Дата выдачи паспорта"
              hide-time-input
              hide-prepend-icon
            />
          </div>
          <v-textarea
            v-model.trim="$v.form.passportIssued.$model"
            variant="outlined"
            label="Паспорт выдан"
            density="compact"
            :rows="4"
          />
        </div>
        <div class="driver-license">
          <v-text-field
            v-model.trim="$v.form.licenseId.$model"
            variant="outlined"
            hide-details
            label="Номер ВУ"
            density="compact"
          />
          <v-text-field
            v-model.trim="$v.form.licenseCategory.$model"
            variant="outlined"
            label="Категории ВУ"
            hide-details
            density="compact"
          />
          <DateTimeInput
            v-model="$v.form.licenseDate.$model"
            label="Дата выдачи ВУ"
            hide-time-input
            hide-prepend-icon
          />
        </div>
        <div class="driver-cards">
          <v-text-field
            v-model.trim="$v.form.inn.$model"
            variant="outlined"
            hide-details
            label="ИНН"
            density="compact"
          />
          <v-text-field
            v-model.trim="$v.form.driverCardId.$model"
            variant="outlined"
            hide-details
            label="Карта водителя"
            density="compact"
          />
          <DateTimeInput
            v-model="$v.form.driverCardPeriod.$model"
            label="КВ действительна до"
            hide-time-input
            hide-prepend-icon
          />
        </div>
      </div>
      <div class="row-wrapper phones">
        <v-text-field
          v-model.trim="$v.form.phone.$model"
          variant="outlined"
