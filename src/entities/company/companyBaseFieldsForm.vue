<template>
  <div>
    <buttons-panel
      panelType="form"
      :disabledSubmit="invalidForm"
      @cancel="cancelHandler"
      @submit="submitHandler"
    />
    <div class="form-wrapper">
      <v-text-field
        v-model="state.fullName"
        label="Полное названии компании"
        density="compact"
        required
        clearable
        variant="outlined"
        :error-messages="fullNameErrorMessages"
        :style="{ maxWidth: '400px' }"
        @blur="v$.fullName.$touch"
      />
    </div>
  </div>
</template>
<script lang="ts">
//@ts-nocheck
import { defineComponent } from 'vue'
import { watch } from 'vue'

import router from '@/router'
import { ButtonsPanel } from '@/shared/ui'

import { useBaseFieldsForm } from './useBaseFieldsForm'
export default defineComponent({
  name: 'CompanyBaseFieldsForm',
  components: { ButtonsPanel },
  props: {
    item: { type: Object, required: true },
  },
  setup(props, ctx) {
    const { v$, state, invalidForm, fullNameErrorMessages, setFormState } =
      useBaseFieldsForm(props.item)

    function cancelHandler() {
      router.go(-1)
    }

    function submitHandler() {
      ctx.emit('submit', state.value)
    }

    function saveHandler() {
      ctx.emit('save', state.value)
    }

    watch(
      () => props.item,
      () => {
        setFormState(props.item)
      },
      { immediate: true }
    )
    return {
      v$,
      cancelHandler,
      state,
      invalidForm,
      fullNameErrorMessages,
      submitHandler,
      saveHandler,
    }
  },
})
</script>
<style scoped>
.form-wrapper {
  display: flex;
  margin: 20px;
  flex-direction: column;
  gap: 10px;
}
</style>
