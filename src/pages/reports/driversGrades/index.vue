<template>
  <div id="wrapper">
    <div id="header">
      <h4>Оценки водителей</h4>
    </div>
    <div id="settings">
      <date-range-input v-model="dateRange" />
      <v-btn size="small" color="primary" @click="getReportHandler">
        <v-icon start> mdi-download </v-icon> Скачать отчет
      </v-btn>
    </div>
    <div id="report-body">
      <v-progress-linear
        v-if="loading"
        indeterminate
        color="primary"
        striped
        rounded
      />
    </div>
  </div>
</template>
<script lang="ts">
//@ts-nocheck
import { defineComponent } from 'vue'
import { ref } from 'vue'

import { ReportService } from '@/shared/services'
import { DateRangeInput } from '@/shared/ui'
import store from '@/store'

import initDateRange from './initDateRange'

export default defineComponent({
  name: 'DriversGradesReport',
  components: {
    DateRangeInput,
  },
  props: {},
  setup() {
    const loading = ref(false)
    const dateRange = ref(initDateRange())

    async function getReportHandler() {
      try {
        loading.value = true
        await ReportService.getDriversGradesReportFile(dateRange.value)
        loading.value = false
      } catch (e) {
        loading.value = false
        store.commit('setError', e.message)
      }
    }
    return {
      dateRange,
      loading,
      getReportHandler,
    }
  },
})
</script>
