<template>
  <v-container fluid>
    <v-row>
      <v-col>
        <app-tkname-form :loading="loading" @submit="submit" @cancel="cancel" />
      </v-col>
    </v-row>
  </v-container>
</template>
<script lang="ts">
//@ts-nocheck
import { defineComponent } from 'vue'

import AppTknameForm from '@/modules/profile/components/tkNameForm'

export default defineComponent({
  name: 'TkNameCreate',
  components: {
    AppTknameForm,
  },
  data() {
    return {
      loading: false,
    }
  },
  methods: {
    submit(tkName) {
      this.loading = true
      this.$store
        .dispatch('createTkName', tkName)
        .then(() => {
          this.loading = false
          this.$router.push({ name: 'TkNameList' })
        })
        .catch((e) => {
          this.loading = false
          this.$store.commit('setError', e)
        })
    },
    cancel() {
      this.$router.go(-1)
    },
  },
})
</script>
